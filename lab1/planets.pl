% 1. Какие планеты ближе к Солнцу, чем Земля?
%  > меркурий (39 у.е.), венера (72 у.е.)
forall((планета(земля, Расстояние_До_Земли, _, _),
        планета(Название, Расстояние, _, _),
        Расстояние @< Расстояние_До_Земли),
    format('~q (~q у.е.) ', [Название, Расстояние])).

% 2. Какие планеты дальше от Солнца, чем Земля?
%  > марс (152 у.е.) юпитер (520 у.е.) сатурн (954 у.е.) уран (1920 у.е.)
%  > нептун (3010 у.е.) плутон (3950 у.е.)
