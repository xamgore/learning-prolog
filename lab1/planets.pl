        планета(Название, Расстояние, _, _),
        Расстояние @> Расстояние_до_Земли),
    format('~q (~q у.е.) ', [Название, Расстояние])).

% 3. На каких планетах солнечные сутки меньше, чем земные?
%  > юпитер сатурн нептун
forall((планета(земля, _, _, Земные_сутки),
        планета(Название, _, _, Сутки),
        Сутки @< Земные_сутки),
    format('~q ', [Название])).

% 4. На каких планетах период обращения измеряется в годах?
%  > земля марс юпитер сатурн уран нептун плутон
